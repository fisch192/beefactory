/// Combined map of all number words (both languages, lowercased).
/// German entries take precedence if there were collisions (there are none).
final Map<String, int> allNumberWords = {
  ...italianNumbers,
  ...germanNumbers,
};

/// Try to parse a token as an integer: first as a literal digit string,
/// then as a number word in either language.
int? parseNumberToken(String token) {
  final asInt = int.tryParse(token);
  if (asInt != null) return asInt;
  return allNumberWords[token.toLowerCase()];
}
